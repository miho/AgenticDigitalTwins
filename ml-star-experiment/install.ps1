<#
.SYNOPSIS
  Installer for the Hamilton STAR Digital Twin, standalone Method Editor, and MCP server.

.DESCRIPTION
  Checks prerequisites (Node.js >= 18, npm), installs npm dependencies, and builds:
    - hamilton-star-twin   (Electron app + headless HTTP server + renderer)
    - hamilton-star-mcp    (MCP server exposing the twin API)

  Also generates launcher scripts in the repo root:
    - run-twin.ps1      Electron digital twin UI
    - run-editor.ps1    Headless server + browser to /protocol (standalone Method Editor)
    - run-mcp.ps1       MCP server (stdio by default, or HTTP with -Port)

.PARAMETER Target
  Which components to install. One of: All, Twin, MCP, Launchers. Default: All.

.PARAMETER Clean
  Remove node_modules and dist folders before installing.

.PARAMETER SkipInstall
  Skip `npm install` (only run build + launchers). Useful when deps are already present.

.PARAMETER SkipBuild
  Skip `npm run build`.

.PARAMETER Port
  HTTP port used by the generated run-editor.ps1 launcher. Default: 8222.

.EXAMPLE
  .\install.ps1
  .\install.ps1 -Target Twin -Clean
  .\install.ps1 -SkipInstall -SkipBuild -Target Launchers
#>
[CmdletBinding()]
param(
  [ValidateSet('All','Twin','MCP','Launchers')]
  [string]$Target = 'All',
  [switch]$Clean,
  [switch]$SkipInstall,
  [switch]$SkipBuild,
  [int]$Port = 8222
)

$ErrorActionPreference = 'Stop'
$RepoRoot  = $PSScriptRoot
$TwinDir   = Join-Path $RepoRoot 'hamilton-star-twin'
$McpDir    = Join-Path $RepoRoot 'hamilton-star-mcp'

function Write-Step    ($msg) { Write-Host "==> $msg" -ForegroundColor Cyan }
function Write-Ok      ($msg) { Write-Host "    $msg" -ForegroundColor Green }
function Write-WarnMsg ($msg) { Write-Host "    $msg" -ForegroundColor Yellow }
function Write-ErrMsg  ($msg) { Write-Host "    $msg" -ForegroundColor Red }

function Test-Command($name) {
  $null = Get-Command $name -ErrorAction SilentlyContinue
  return $?
}

function Assert-Prereqs {
  Write-Step 'Checking prerequisites'

  if (-not (Test-Command node)) {
    throw 'Node.js not found in PATH. Install Node.js 18+ from https://nodejs.org/ and re-run.'
  }
  $nodeVer = (& node --version).TrimStart('v')
  $major   = [int]($nodeVer.Split('.')[0])
  if ($major -lt 18) {
    throw "Node.js $nodeVer found, but >= 18 is required."
  }
  Write-Ok "node $nodeVer"

  if (-not (Test-Command npm)) { throw 'npm not found in PATH.' }
  $npmVer = (& npm --version).Trim()
  Write-Ok "npm $npmVer"

  if (-not (Test-Path $TwinDir)) { throw "Missing directory: $TwinDir" }
  if (-not (Test-Path $McpDir))  { throw "Missing directory: $McpDir"  }
}

function Invoke-InDir {
  param([string]$Dir, [scriptblock]$Block)
  Push-Location $Dir
  try { & $Block } finally { Pop-Location }
}

function Remove-Dir($path) {
  if (Test-Path $path) {
    Write-WarnMsg "removing $path"
    Remove-Item -Recurse -Force $path
  }
}

function Install-Component {
  param(
    [string]$Name,
    [string]$Dir,
    [string]$BuildScript = 'build'
  )
  Write-Step "$Name  ($Dir)"
  Invoke-InDir $Dir {
    if ($Clean) {
      Remove-Dir (Join-Path $Dir 'node_modules')
      Remove-Dir (Join-Path $Dir 'dist')
    }
    if (-not $SkipInstall) {
      Write-Ok 'npm install'
      & npm install
      if ($LASTEXITCODE -ne 0) { throw "npm install failed for $Name" }
    } else {
      Write-WarnMsg 'skipping npm install'
    }
    if (-not $SkipBuild) {
      Write-Ok "npm run $BuildScript"
      & npm run $BuildScript
      if ($LASTEXITCODE -ne 0) { throw "npm run $BuildScript failed for $Name" }
    } else {
      Write-WarnMsg 'skipping build'
    }
  }
  Write-Ok "$Name ready"
}

function Write-Launcher {
  param([string]$Path, [string]$Content)
  Set-Content -Path $Path -Value $Content -Encoding UTF8
  Write-Ok "wrote $(Split-Path $Path -Leaf)"
}

function Write-Launchers {
  Write-Step 'Generating launcher scripts'

  $twinLauncher = @"
# Auto-generated by install.ps1 — launches the Electron digital twin UI.
`$ErrorActionPreference = 'Stop'
Push-Location `"$TwinDir`"
try { & npx electron dist/main/main.js `$args } finally { Pop-Location }
"@
  Write-Launcher (Join-Path $RepoRoot 'run-twin.ps1') $twinLauncher

  $editorLauncher = @"
# Auto-generated by install.ps1 — starts the headless HTTP server and opens the
# standalone Method Editor (served at /protocol) in the default browser.
param([int]`$Port = $Port)
`$ErrorActionPreference = 'Stop'
Push-Location `"$TwinDir`"
try {
  `$srv = Start-Process -FilePath 'node' -ArgumentList @('dist/headless/server.js','--port',`$Port) -PassThru -NoNewWindow
  Start-Sleep -Seconds 2
  Start-Process `"http://localhost:`$Port/protocol`"
  Write-Host 'Method Editor running. Press Ctrl+C in this window to stop the server.' -ForegroundColor Cyan
  Wait-Process -Id `$srv.Id
} finally { Pop-Location }
"@
  Write-Launcher (Join-Path $RepoRoot 'run-editor.ps1') $editorLauncher

  $mcpLauncher = @"
# Auto-generated by install.ps1 — runs the MCP server.
#   .\run-mcp.ps1              # stdio transport (for Claude Code / Claude Desktop)
#   .\run-mcp.ps1 -Port 8100   # HTTP transport
param([int]`$Port = 0)
`$ErrorActionPreference = 'Stop'
Push-Location `"$McpDir`"
try {
  if (`$Port -gt 0) { & node dist/index.js --port `$Port }
  else             { & node dist/index.js }
} finally { Pop-Location }
"@
  Write-Launcher (Join-Path $RepoRoot 'run-mcp.ps1') $mcpLauncher
}

# -----------------------------------------------------------------------------

Write-Host ''
Write-Host 'Hamilton STAR Digital Twin — Installer' -ForegroundColor Magenta
Write-Host "Repo: $RepoRoot"
Write-Host ''

Assert-Prereqs

switch ($Target) {
  'Twin'       { Install-Component 'Digital Twin'  $TwinDir }
  'MCP'        { Install-Component 'MCP Server'    $McpDir  }
  'Launchers'  { }
  'All' {
    Install-Component 'Digital Twin' $TwinDir
    Install-Component 'MCP Server'   $McpDir
  }
}

Write-Launchers

Write-Host ''
Write-Host 'Done.' -ForegroundColor Green
Write-Host ''
Write-Host 'Next steps:' -ForegroundColor Cyan
Write-Host '  .\run-twin.ps1              Electron digital twin UI'
Write-Host "  .\run-editor.ps1            Standalone Method Editor (http://localhost:$Port/protocol)"
Write-Host '  .\run-mcp.ps1               MCP server (stdio)'
Write-Host '  .\run-mcp.ps1 -Port 8100    MCP server (HTTP)'
Write-Host ''
