// --- runtime/executor/ContinuousExecutor.js ---
/**
 * Executor that continuously processes events using the JavaScript event loop.
 *
 * This executor uses event-driven wakeup for delayed events: when a timer fires,
 * the RuntimeContext notifies the executor via a wakeup listener, which schedules
 * immediate event processing. No polling is needed.
 *
 * @example
 * const executor = new ContinuousExecutor(machine);
 * executor.start();
 *
 * // Fire events asynchronously
 * await executor.sendAsync('go');
 * await executor.sendAsync('next');
 *
 * // Stop when done
 * executor.stop();
 *
 * @example
 * // With delayed events
 * const executor = new ContinuousExecutor(machine);
 * executor.start();
 *
 * // Machine has <send delay="1s" event="timeout"/>
 * await executor.sendAsync('start');
 *
 * // Wait for delayed events to complete
 * await executor.waitForCompletion();
 * executor.stop();
 *
 * @extends StateMachineExecutor
 */
class ContinuousExecutor extends StateMachineExecutor {
  /**
   * Creates a new continuous executor.
   * @param {Object} machine - The state machine to execute
   */
  constructor(machine) {
    super(machine);
    this._started = false;
    this._stopped = false;
    this._running = false;
    this._pendingPromises = [];
    this._eventQueue = [];
    this._processing = false;
    this._wakeupScheduled = false;
  }

  /**
   * Whether the executor is currently running.
   * @returns {boolean}
   */
  get isRunning() {
    return this._running && !this._stopped;
  }

  /**
   * Starts the state machine and registers the wakeup listener for delayed events.
   * @param {Object} [initData] - Optional initialization data
   * @throws {Error} If already started or stopped
   */
  start(initData) {
    this._ensureStartAllowed();

    if (this._machine.start) {
      this._machine.start(initData);
    }

    this._started = true;
    this._running = true;

    // Register wakeup listener for event-driven timer processing
    this._registerWakeupListener();
  }

  /**
   * Stops the executor and cancels any pending operations.
   */
  stop() {
    this._stopped = true;
    this._running = false;

    // Deregister wakeup listener
    this._deregisterWakeupListener();

    // Reject any pending promises
    for (const { reject } of this._pendingPromises) {
      reject(new Error('Executor stopped'));
    }
    this._pendingPromises = [];
    this._eventQueue = [];
  }

  /**
   * Fires an external event synchronously (blocks until macrostep completes).
   * Note: In continuous mode, this still processes synchronously.
   * @param {string} eventName - Name of the event
   * @param {*} [data] - Optional event data
   */
  send(eventName, data) {
    this._ensureRunning();
    this._processEvent(eventName, data);
  }

  /**
   * Fires an external event asynchronously.
   * @param {string} eventName - Name of the event
   * @param {*} [data] - Optional event data
   * @returns {Promise<void>} Resolves when macrostep completes
   */
  sendAsync(eventName, data) {
    return new Promise((resolve, reject) => {
      if (!this._started) {
        reject(new Error('State machine has not been started'));
        return;
      }
      if (this._stopped) {
        reject(new Error('Executor has been stopped'));
        return;
      }

      // Queue the event for processing
      this._eventQueue.push({ eventName, data, resolve, reject });
      this._scheduleProcessing();
    });
  }

  /**
   * Pumps any pending delayed events.
   * @returns {boolean} True if any events were processed
   */
  pumpEvents() {
    if (!this._started || this._stopped) return false;

    // Move any due events from scheduledEvents into the external queue
    const ctx = this._machine.ctx || this._machine._context;
    if (ctx && ctx.processDueEvents) {
      ctx.processDueEvents();
    }

    // Process all queued events (internal + external).
    // Timer callbacks deposit events directly into externalQueue, so we
    // must always drain the queue — not only when processDueEvents found work.
    if (this._machine._processEventQueue) {
      this._machine._processEventQueue();
      return true;
    }
    return false;
  }

  /**
   * Waits for all pending events (including delayed) to complete.
   * Useful for testing scenarios with delayed sends.
   * @param {number} [timeout=30000] - Maximum wait time in ms
   * @returns {Promise<void>}
   */
  waitForCompletion(timeout = 30000) {
    return new Promise((resolve, reject) => {
      const startTime = Date.now();

      const check = () => {
        if (this._stopped) {
          reject(new Error('Executor stopped'));
          return;
        }

        if (this.isFinished) {
          resolve();
          return;
        }

        // Check if there are pending delayed events
        const ctx = this._machine.ctx || this._machine._context;
        const hasPendingDelayed = ctx?.scheduledEvents?.size > 0;
        const hasPendingQueue = this._eventQueue.length > 0;

        if (!hasPendingDelayed && !hasPendingQueue) {
          resolve();
          return;
        }

        if (Date.now() - startTime > timeout) {
          reject(new Error('Timeout waiting for completion'));
          return;
        }

        // Wait for next wakeup or check periodically as fallback
        setTimeout(check, 50);
      };

      check();
    });
  }

  /**
   * Register a state change callback on the executor.
   * Delegates to the machine's onStateChange if available.
   * @param {function} callback - Called with (enteredStates, exitedStates)
   * @returns {function} Unsubscribe function
   */
  onStateChange(callback) {
    if (this._machine.onStateChange) {
      return this._machine.onStateChange(callback);
    }
    // Fallback: no-op unsubscribe
    return () => {};
  }

  /**
   * @private
   */
  _processEvent(eventName, data) {
    if (this._machine.send) {
      this._machine.send(eventName, data);
    }
  }

  /**
   * @private
   */
  _scheduleProcessing() {
    if (this._processing) return;

    // Use setImmediate if available (Node.js), otherwise setTimeout
    const schedule = typeof setImmediate !== 'undefined' ? setImmediate : (fn) => setTimeout(fn, 0);

    schedule(() => this._processQueue());
  }

  /**
   * @private
   */
  _processQueue() {
    if (this._stopped || this._processing) return;

    this._processing = true;

    try {
      while (this._eventQueue.length > 0 && !this._stopped) {
        const { eventName, data, resolve, reject } = this._eventQueue.shift();
        try {
          this._processEvent(eventName, data);
          resolve();
        } catch (e) {
          reject(e);
        }
      }
    } finally {
      this._processing = false;
    }
  }

  /**
   * Register wakeup listener on the RuntimeContext for event-driven timer processing.
   * When a delayed event timer fires, the RuntimeContext calls this listener,
   * which schedules event processing on the next microtask.
   * @private
   */
  _registerWakeupListener() {
    const ctx = this._machine.ctx || this._machine._context;
    if (ctx && ctx.setWakeupListener) {
      ctx.setWakeupListener(() => {
        if (this._stopped || this._wakeupScheduled) return;
        this._wakeupScheduled = true;
        // Use queueMicrotask so events are processed immediately after the
        // current timer callback, before any other awaiting code runs.
        // This matches the behavior of Java/Python/Go executors where the
        // dedicated thread processes events as soon as they arrive.
        queueMicrotask(() => {
          this._wakeupScheduled = false;
          if (!this._stopped) {
            this.pumpEvents();
          }
        });
      });
    }
  }

  /**
   * Deregister wakeup listener on cleanup.
   * @private
   */
  _deregisterWakeupListener() {
    const ctx = this._machine.ctx || this._machine._context;
    if (ctx && ctx.setWakeupListener) {
      ctx.setWakeupListener(null);
    }
  }

  /**
   * @private
   */
  _ensureStartAllowed() {
    if (this._started) {
      throw new Error('State machine has already been started');
    }
    if (this._stopped) {
      throw new Error('Executor has been stopped');
    }
  }

  /**
   * @private
   */
  _ensureRunning() {
    if (!this._started) {
      throw new Error('State machine has not been started');
    }
    if (this._stopped) {
      throw new Error('Executor has been stopped');
    }
  }
}

// --- runtime/executor/RunToCompletionExecutor.js ---
/**
 * Executor that uses run-to-completion semantics.
 *
 * All calls are executed synchronously on the caller's thread/context.
 * Each send() call runs a complete macrostep before returning.
 *
 * This is the simplest executor model, suitable for:
 * - Unit testing
 * - Simple synchronous workflows
 * - Environments without async support
 *
 * @example
 * const executor = new RunToCompletionExecutor(machine);
 * executor.start();
 * executor.send('go');
 * console.log(executor.getActiveStateIds());
 *
 * @extends StateMachineExecutor
 */
class RunToCompletionExecutor extends StateMachineExecutor {
  /**
   * Creates a new run-to-completion executor.
   * @param {Object} machine - The state machine to execute
   */
  constructor(machine) {
    super(machine);
    this._started = false;
    this._stopped = false;
  }

  /**
   * Whether the executor is currently running.
   * @returns {boolean}
   */
  get isRunning() {
    return this._started && !this._stopped && !this.isFinished;
  }

  /**
   * Starts the state machine.
   * @param {Object} [initData] - Optional initialization data
   * @throws {Error} If already started or stopped
   */
  start(initData) {
    this._ensureStartAllowed();
    if (this._machine.start) {
      this._machine.start(initData);
    }
    this._started = true;
  }

  /**
   * Stops the executor.
   */
  stop() {
    this._stopped = true;
  }

  /**
   * Fires an external event synchronously.
   * @param {string} eventName - Name of the event
   * @param {*} [data] - Optional event data
   * @throws {Error} If not running
   */
  send(eventName, data) {
    this._ensureRunning();
    if (this._machine.send) {
      this._machine.send(eventName, data);
    } else if (this._machine.send) {
      this._machine.send(eventName, data);
    }
  }

  /**
   * Fires an external event and returns a resolved Promise.
   * Since this is synchronous, the Promise is already resolved when returned.
   * @param {string} eventName - Name of the event
   * @param {*} [data] - Optional event data
   * @returns {Promise<void>}
   */
  sendAsync(eventName, data) {
    try {
      this.send(eventName, data);
      return Promise.resolve();
    } catch (e) {
      return Promise.reject(e);
    }
  }

  /**
   * Pumps any pending delayed events.
   * @returns {boolean} True if any events were processed
   */
  pumpEvents() {
    this._ensureRunning();
    if (this._machine.pumpEvents) {
      this._machine.pumpEvents();
      return true;
    }
    if (this._machine._context && this._machine._context.processDueEvents) {
      return this._machine._context.processDueEvents();
    }
    return false;
  }

  /**
   * @private
   */
  _ensureStartAllowed() {
    if (this._started) {
      throw new Error('State machine has already been started');
    }
    if (this._stopped) {
      throw new Error('Executor has been stopped');
    }
  }

  /**
   * @private
   */
  _ensureRunning() {
    if (!this._started) {
      throw new Error('State machine has not been started');
    }
    if (this._stopped) {
      throw new Error('Executor has been stopped');
    }
  }
}

