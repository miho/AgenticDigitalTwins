/**
 * Production-ready TraceListener that writes trace events to JSONL format.
 *
 * Each trace event is written as a single JSON object per line (JSON Lines format),
 * compatible with Java's JsonlTraceWriter and C's scxml_trace_jsonl_writer for
 * cross-target trace playback and analysis.
 *
 * Output format:
 * ```json
 * {"timestamp":0,"type":"session_start","session_id":"abc123","scxml_name":"TrafficLight","datamodel":"ecmascript"}
 * {"timestamp":1234,"type":"state_enter","state_id":"red"}
 * {"timestamp":2345,"type":"transition","from":"red","to":"green","event":"timer"}
 * {"timestamp":3456,"type":"state_exit","state_id":"green","invoke_id":"childMachine"}
 * ```
 *
 * @example
 * // Node.js usage with file output
 * import fs from 'fs';
 * const stream = fs.createWriteStream('trace.jsonl');
 * const writer = new JsonlTraceWriter(line => stream.write(line + '\n'));
 * machine.addTraceListener(writer);
 * machine.start();
 * // ... when done:
 * writer.flush();
 * stream.end();
 *
 * @example
 * // Browser usage with array collection
 * const lines = [];
 * const writer = new JsonlTraceWriter(line => lines.push(line));
 * machine.addTraceListener(writer);
 * machine.start();
 * // Download as file:
 * const blob = new Blob([lines.join('\n')], { type: 'application/x-ndjson' });
 */
class JsonlTraceWriter extends InvokeAwareTraceListener {
  /**
   * Creates a new JSONL trace writer.
   *
   * @param {Function} writeLine - Function called with each JSON line (without newline)
   * @param {Object} [options] - Writer options
   * @param {boolean} [options.includeActiveStates=false] - Include active states array in events
   * @param {boolean} [options.prettyPrint=false] - Pretty-print JSON (not recommended for production)
   */
  constructor(writeLine, options = {}) {
    super();
    this._writeLine = writeLine;
    this._includeActiveStates = options.includeActiveStates || false;
    this._prettyPrint = options.prettyPrint || false;
    this._buffer = [];
    this._autoFlush = options.autoFlush !== false;
  }

  /**
   * Get current timestamp in milliseconds.
   * Override for custom timestamp source.
   * @returns {number}
   */
  _getTimestamp() {
    return Date.now();
  }

  /**
   * Write a JSON object as a line.
   * @private
   */
  _write(obj) {
    const json = this._prettyPrint
      ? JSON.stringify(obj, null, 2)
      : JSON.stringify(obj);

    if (this._autoFlush) {
      this._writeLine(json);
    } else {
      this._buffer.push(json);
    }
  }

  /**
   * Flush buffered lines (only needed if autoFlush is false).
   */
  flush() {
    for (const line of this._buffer) {
      this._writeLine(line);
    }
    this._buffer = [];
  }

  /**
   * Get buffered lines (for testing or batch processing).
   * @returns {string[]}
   */
  getBuffer() {
    return [...this._buffer];
  }

  // Invoke-aware trace methods

  onSessionStartWithInvoke(sessionId, machineName, datamodel, timestamp, invokeId) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'session_start',
      session_id: sessionId,
      scxml_name: machineName,
      datamodel: datamodel || 'ecmascript'
    };
    if (invokeId) obj.invoke_id = invokeId;
    this._write(obj);
  }

  onSessionEndWithInvoke(sessionId, finalStates, timestamp, invokeId) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'session_end',
      final_states: finalStates
    };
    if (invokeId) obj.invoke_id = invokeId;
    this._write(obj);
  }

  onStateEnterWithInvoke(stateId, activeStates, timestamp, invokeId) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'state_enter',
      state_id: stateId
    };
    if (this._includeActiveStates) obj.active_states = activeStates;
    if (invokeId) obj.invoke_id = invokeId;
    this._write(obj);
  }

  onStateExitWithInvoke(stateId, activeStates, timestamp, invokeId) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'state_exit',
      state_id: stateId
    };
    if (this._includeActiveStates) obj.active_states = activeStates;
    if (invokeId) obj.invoke_id = invokeId;
    this._write(obj);
  }

  onTransitionExecuteWithInvoke(sourceId, targetIds, event, cond, timestamp, invokeId) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'transition',
      from: sourceId,
      to: Array.isArray(targetIds) ? targetIds[0] : targetIds,
      event: event
    };
    if (invokeId) obj.invoke_id = invokeId;
    this._write(obj);
  }

  onEventReceivedWithInvoke(eventName, eventType, eventData, timestamp, invokeId) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'event_received',
      event_name: eventName,
      event_type: eventType
    };
    if (eventData !== undefined && eventData !== null) {
      obj.event_data = this._serializeValue(eventData);
    }
    if (invokeId) obj.invoke_id = invokeId;
    this._write(obj);
  }

  onEventProcessedWithInvoke(eventName, handled, timestamp, invokeId) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'event_processed',
      event_name: eventName,
      handled: handled
    };
    if (invokeId) obj.invoke_id = invokeId;
    this._write(obj);
  }

  onVariableChangeWithInvoke(name, oldValue, newValue, timestamp, invokeId) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'variable_changed',
      variable_name: name,
      old_value: this._serializeValue(oldValue),
      new_value: this._serializeValue(newValue)
    };
    if (invokeId) obj.invoke_id = invokeId;
    this._write(obj);
  }

  onActionExecuteWithInvoke(actionType, details, timestamp, invokeId) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'action_execute',
      action_type: actionType,
      details: details
    };
    if (invokeId) obj.invoke_id = invokeId;
    this._write(obj);
  }

  // Additional trace events (not invoke-aware in base interface)

  onError(errorType, message, location, timestamp) {
    this._write({
      timestamp: timestamp || this._getTimestamp(),
      type: 'error',
      error_type: errorType,
      message: message,
      location: location
    });
  }

  onLog(label, message, timestamp) {
    this._write({
      timestamp: timestamp || this._getTimestamp(),
      type: 'log',
      label: label,
      message: message
    });
  }

  onInvokeStart(invokeId, type, parentStateId, timestamp) {
    this._write({
      timestamp: timestamp || this._getTimestamp(),
      type: 'invoke_start',
      invoke_id: invokeId,
      invoke_type: type,
      parent_state: parentStateId
    });
  }

  onInvokeEnd(invokeId, donedata, timestamp) {
    const obj = {
      timestamp: timestamp || this._getTimestamp(),
      type: 'invoke_end',
      invoke_id: invokeId
    };
    if (donedata !== undefined && donedata !== null) {
      obj.donedata = this._serializeValue(donedata);
    }
    this._write(obj);
  }

  onInvokeCancel(invokeId, timestamp) {
    this._write({
      timestamp: timestamp || this._getTimestamp(),
      type: 'invoke_cancel',
      invoke_id: invokeId
    });
  }

  onMacrostepStart(triggeringEvent, timestamp) {
    this._write({
      timestamp: timestamp || this._getTimestamp(),
      type: 'macrostep_start',
      triggering_event: triggeringEvent
    });
  }

  onMacrostepEnd(activeStates, timestamp) {
    this._write({
      timestamp: timestamp || this._getTimestamp(),
      type: 'macrostep_end',
      active_states: activeStates
    });
  }

  /**
   * Serialize a value for JSON output.
   * @private
   */
  _serializeValue(value) {
    if (value === undefined) return null;
    if (value === null) return null;
    if (typeof value === 'function') return `[Function: ${value.name || 'anonymous'}]`;
    if (value instanceof Error) return { error: value.message };

    try {
      // Test if serializable
      JSON.stringify(value);
      return value;
    } catch (e) {
      return String(value);
    }
  }
}

/**
 * Creates a JsonlTraceWriter that writes to the console.
 * Useful for debugging.
 *
 * @param {Object} [options] - Writer options
 * @returns {JsonlTraceWriter}
 */
export function createConsoleJsonlWriter(options = {}) {
  return new JsonlTraceWriter(line => console.log(line), options);
}

/**
 * Creates a JsonlTraceWriter that collects lines in an array.
 * Useful for testing or batch processing.
 *
 * @param {string[]} lines - Array to collect lines into
 * @param {Object} [options] - Writer options
 * @returns {JsonlTraceWriter}
 */
export function createArrayJsonlWriter(lines, options = {}) {
  return new JsonlTraceWriter(line => lines.push(line), options);
}

