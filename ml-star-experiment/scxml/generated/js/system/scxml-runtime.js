/**
 * Event object as per W3C SCXML specification
 */
class ScxmlEvent {
  constructor(name, data = null, options = {}) {
    this.name = name;
    // Per W3C spec, _event.data should be undefined when no data is provided
    // This allows tests like `typeof _event.data === 'undefined'` to work
    this.data = data !== null ? data : undefined;
    this.type = options.type || 'external';
    this.sendid = options.sendid || undefined;
    this.origin = options.origin || undefined;
    this.origintype = options.origintype || undefined;
    this.invokeid = options.invokeid || undefined;
  }
}

/**
 * Runtime context for state machine execution
 */
class RuntimeContext {
  constructor(machine) {
    this.machine = machine;
    this.internalQueue = [];
    this.externalQueue = [];
    this.scheduledEvents = new Map(); // id -> {time, event, timer}
    this.nextSendId = 1;
    this.invokedMachines = new Map(); // invokeid -> machine
    this._wakeupListener = null;
  }

  /**
   * Set a wakeup listener that is called when a delayed event becomes due.
   * Used by ContinuousExecutor for event-driven timer processing.
   * @param {Function|null} listener - Callback to invoke on timer fire
   */
  setWakeupListener(listener) {
    this._wakeupListener = listener;
  }

  /**
   * Queue an internal event (processed in current macrostep)
   */
  raiseEvent(name, data = null, type = 'internal') {
    this.internalQueue.push(new ScxmlEvent(name, data, { type }));
  }

  /**
   * Queue a platform event (for error events generated by the platform)
   */
  raisePlatformEvent(name, options = null) {
    const eventOptions = { type: 'platform' };
    if (options && options.sendid) {
      eventOptions.sendid = options.sendid;
    }
    this.internalQueue.push(new ScxmlEvent(name, null, eventOptions));
  }

  /**
   * Parse a delay value (can be number in ms or string like '1s', '500ms')
   */
  _parseDelay(delay) {
    if (typeof delay === 'number') return delay;
    if (typeof delay !== 'string') return 0;

    delay = delay.trim();
    if (delay.endsWith('ms')) {
      return parseFloat(delay.slice(0, -2).trim()) || 0;
    } else if (delay.endsWith('s')) {
      const seconds = parseFloat(delay.slice(0, -1).trim());
      return (seconds * 1000) || 0;
    }
    return parseFloat(delay) || 0;
  }

  /**
   * Queue an external event (processed in next macrostep)
   */
  sendEvent(name, data = null, options = {}) {
    // Per W3C SCXML spec: Parse data content appropriately
    let processedData = data;
    if (typeof data === 'string') {
      const trimmed = data.trim();
      // Check for JSON
      if ((trimmed.startsWith('{') && trimmed.endsWith('}')) ||
          (trimmed.startsWith('[') && trimmed.endsWith(']'))) {
        try {
          processedData = JSON.parse(trimmed);
        } catch (e) {
          // Not valid JSON, use as-is
        }
      }
      // Check for XML (with <?xml?> marker or starts with <)
      else if (trimmed.startsWith('<?xml?>') || (trimmed.startsWith('<') && !trimmed.startsWith('<?xml version'))) {
        try {
          // Remove <?xml?> marker if present
          let xmlContent = trimmed.startsWith('<?xml?>') ? trimmed.substring(7) : trimmed;
          // Parse XML to DOM using DOMParser
          const parser = typeof DOMParser !== 'undefined' ? new DOMParser() : null;
          if (parser) {
            const doc = parser.parseFromString(xmlContent, 'text/xml');
            // Check for parse errors
            if (!doc.querySelector('parsererror')) {
              processedData = doc.documentElement;
            }
          }
        } catch (e) {
          // Not valid XML, use as-is
        }
      }
    }

    const event = new ScxmlEvent(name, processedData, { type: 'external', ...options });

    const delayMs = this._parseDelay(options.delay);
    if (delayMs > 0) {
      const id = options.id || `__send${this.nextSendId++}`;
      const time = Date.now() + delayMs;
      // Schedule a setTimeout to fire the event at the exact time
      const timer = setTimeout(() => {
        const entry = this.scheduledEvents.get(id);
        if (entry) {
          this.scheduledEvents.delete(id);
          this.externalQueue.push(entry.event);
          // Notify executor that an event is ready
          if (this._wakeupListener) {
            this._wakeupListener();
          }
        }
      }, delayMs);
      this.scheduledEvents.set(id, { time, event, timer });
      return id;
    } else {
      this.externalQueue.push(event);
      return null;
    }
  }

  /**
   * Cancel a scheduled event
   */
  cancelEvent(sendid) {
    const entry = this.scheduledEvents.get(sendid);
    if (entry) {
      if (entry.timer) {
        clearTimeout(entry.timer);
      }
      this.scheduledEvents.delete(sendid);
    }
  }

  /**
   * Get next scheduled event time
   */
  getNextScheduledTime() {
    let minTime = null;
    for (const [, scheduled] of this.scheduledEvents) {
      if (minTime === null || scheduled.time < minTime) {
        minTime = scheduled.time;
      }
    }
    return minTime;
  }

  /**
   * Process any due scheduled events
   */
  processDueEvents() {
    const now = Date.now();
    const dueEvents = [];

    for (const [id, scheduled] of this.scheduledEvents) {
      if (scheduled.time <= now) {
        dueEvents.push({ id, event: scheduled.event, timer: scheduled.timer });
      }
    }

    for (const { id, event, timer } of dueEvents) {
      if (timer) {
        clearTimeout(timer);
      }
      this.scheduledEvents.delete(id);
      this.externalQueue.push(event);
    }

    return dueEvents.length > 0;
  }

  /**
   * Cancel all scheduled events (for cleanup)
   */
  cancelAllScheduledEvents() {
    for (const [, entry] of this.scheduledEvents) {
      if (entry.timer) {
        clearTimeout(entry.timer);
      }
    }
    this.scheduledEvents.clear();
  }
}

/**
 * Base class for transpiled SCXML state machines
 */
class ScxmlStateMachine {
  constructor() {
    // State tracking
    this.activeStates = new Set();
    this.historyValues = new Map(); // stateId -> Set of state indices

    // Runtime context
    this.ctx = new RuntimeContext(this);

    // System variables
    this._sessionid = `session_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
    this._name = '';
    // SCXML I/O processor with both full URI and shorthand access
    const scxmlProcessor = {
      location: '#_scxml_' + this._sessionid
    };
    this._ioprocessors = {
      'http://www.w3.org/TR/scxml/#SCXMLEventProcessor': scxmlProcessor,
      'scxml': scxmlProcessor  // Shorthand access per W3C spec
    };

    // Current event being processed
    this._event = null;

    // Finished flag
    this._finished = false;

    // State change listeners
    this._listeners = [];

    // Logger (can be overridden)
    this._logger = console.log.bind(console);

    // Data model (set by generated class)
    this._datamodel = {};

    // Script function sources (populated by _execScript).
    // Stores function source code (via Function.prototype.toString()) rather than
    // function objects, so that functions are re-created with fresh datamodel
    // bindings on each evaluation — avoiding stale closure captures.
    this._scriptFunctionSources = {};
  }

  // ============================================
  // Public API
  // ============================================

  /**
   * Start the state machine
   */
  start() {
    if (this.activeStates.size > 0) {
      throw new Error('State machine already started');
    }
    // Process any datamodel initialization errors
    if (this._initErrors && this._initErrors.length > 0) {
      for (const errEvent of this._initErrors) {
        this.ctx.raiseEvent(errEvent);
      }
      this._initErrors = null;
    }
    this._enterInitialConfiguration();
    this._processEventQueue();
  }

  /**
   * Send an external event to the state machine
   * @param {string} name - Event name
   * @param {*} data - Event data
   * @param {Object} options - Event options (origin, origintype, etc.)
   */
  send(name, data = null, options = {}) {
    if (this._finished) return;
    this.ctx.externalQueue.push(new ScxmlEvent(name, data, { type: 'external', ...options }));
    this._processEventQueue();
  }

  /**
   * Check if the state machine has finished (reached a top-level final state)
   */
  isFinished() {
    return this._finished;
  }

  /**
   * Get the set of currently active state IDs
   */
  getActiveStateIds() {
    return new Set(this.activeStates);
  }

  /**
   * Check if a state is currently active (for In() predicate)
   */
  isInState(stateId) {
    return this.activeStates.has(stateId);
  }

  /**
   * Returns all events this state machine can react to.
   * Override in generated subclasses with the actual event set.
   * @returns {Set<string>}
   */
  getAllEvents() {
    return new Set();
  }

  /**
   * Returns events currently enabled based on active states and guard conditions.
   * Override in generated subclasses with state-aware logic.
   * @returns {Set<string>}
   */
  getEnabledEvents() {
    return new Set();
  }

  /**
   * Returns all events a specific state can react to (including ancestor transitions).
   * Override in generated subclasses with the actual per-state event set.
   * @param {string} stateId
   * @returns {Set<string>}
   */
  getEventsForState(stateId) {
    return new Set();
  }

  /**
   * Returns events currently enabled for a specific active state (guard-aware).
   * Override in generated subclasses with state-aware guard-evaluating logic.
   * @param {string} stateId
   * @returns {Set<string>}
   */
  getEnabledEventsForState(stateId) {
    return new Set();
  }

  /**
   * Subscribe to state changes
   * @param {Function} callback - Called with (enteredStates, exitedStates)
   * @returns {Function} Unsubscribe function
   */
  onStateChange(callback) {
    this._listeners.push(callback);
    return () => {
      const idx = this._listeners.indexOf(callback);
      if (idx >= 0) this._listeners.splice(idx, 1);
    };
  }

  /**
   * Set the logger function
   */
  setLogger(logger) {
    this._logger = logger || (() => {});
  }

  /**
   * Process any scheduled delayed events
   * @returns {boolean} True if any events were processed
   */
  processDelayedEvents() {
    if (this._finished) return false;
    const processed = this.ctx.processDueEvents();
    if (processed) {
      this._processEventQueue();
    }
    return processed;
  }

  /**
   * Process any pending events in the queues (external and internal).
   * Use this as the wakeup callback for delayed-event auto-processing:
   *
   *   sm.ctx.setWakeupListener(() => sm.pumpEvents());
   */
  pumpEvents() {
    if (this._finished) return;
    this._processEventQueue();
  }

  // ============================================
  // Internal methods (used by generated code)
  // ============================================

  /**
   * Enter initial configuration (overridden by generated class)
   */
  _enterInitialConfiguration() {
    throw new Error('_enterInitialConfiguration must be implemented by generated class');
  }

  /**
   * Process internal and external event queues
   */
  _processEventQueue() {
    // Process internal events first (microsteps)
    while (this.ctx.internalQueue.length > 0 && !this._finished) {
      const event = this.ctx.internalQueue.shift();
      this._processEvent(event);
    }

    // Then process external events
    while (this.ctx.externalQueue.length > 0 && !this._finished) {
      const event = this.ctx.externalQueue.shift();

      // Per W3C spec: If event has invokeid, filter out if it doesn't match an active invoke
      // Events from cancelled invokes should be discarded, with special handling for done.invoke
      if (event.invokeid) {
        const invokeInfo = this.ctx.invokedMachines.get(event.invokeid);
        // If invoke doesn't exist, filter the event
        if (!invokeInfo) {
          continue; // Skip this event
        }
        // done.invoke events should always be delivered (even after cancel due to normal completion)
        const isDoneInvoke = event.name && event.name.startsWith('done.invoke.');
        // If invoke was cancelled and this is NOT a done.invoke event, filter it
        // This handles the case where parent transitions before events from child are processed
        if (invokeInfo.cancelled && !isDoneInvoke) {
          continue; // Skip this event
        }
        // Call finalize for events from non-cancelled invokes
        if (!invokeInfo.cancelled) {
          this._callFinalize(event.invokeid, event);
        }
      }

      // Forward event to children with autoforward enabled
      this._forwardToInvokes(event);

      this._processEvent(event);

      // After each external event, process all resulting internal events
      while (this.ctx.internalQueue.length > 0 && !this._finished) {
        const internalEvent = this.ctx.internalQueue.shift();
        this._processEvent(internalEvent);
      }
    }
  }

  /**
   * Call finalize callback for an invoked machine's event
   */
  _callFinalize(invokeid, event) {
    const invokeInfo = this.ctx.invokedMachines.get(invokeid);
    if (invokeInfo && invokeInfo.finalize) {
      try {
        invokeInfo.finalize(event);
      } catch (e) {
        // Errors in finalize should raise error.execution
        this.ctx.raisePlatformEvent('error.execution');
      }
    }
  }

  /**
   * Process a single event (overridden by generated class)
   */
  _processEvent(event) {
    throw new Error('_processEvent must be implemented by generated class');
  }

  /**
   * Enter a state
   */
  _enterState(stateId) {
    const wasActive = this.activeStates.has(stateId);
    this.activeStates.add(stateId);
    if (!wasActive) {
      this._notifyStateChange([stateId], []);
    }
  }

  /**
   * Exit a state
   */
  _exitState(stateId) {
    const wasActive = this.activeStates.has(stateId);
    this.activeStates.delete(stateId);
    if (wasActive) {
      this._notifyStateChange([], [stateId]);
    }
  }

  /**
   * Record history for a state
   */
  _recordHistory(historyId, stateIds) {
    this.historyValues.set(historyId, new Set(stateIds));
  }

  /**
   * Get recorded history
   */
  _getHistory(historyId) {
    return this.historyValues.get(historyId) || new Set();
  }

  /**
   * Mark the machine as finished.
   * If this is an invoked child, sends done.invoke.<invokeid> to parent.
   */
  _setFinished(doneData = null) {
    this._finished = true;

    // If this is an invoked child, notify parent
    if (this._parentMachine && this._invokeid) {
      const eventName = 'done.invoke.' + this._invokeid;
      this._parentMachine.ctx.externalQueue.push(new ScxmlEvent(eventName, doneData, {
        type: 'external',
        invokeid: this._invokeid
      }));
    }
  }

  /**
   * Notify listeners of state changes
   */
  _notifyStateChange(entered, exited) {
    for (const listener of this._listeners) {
      try {
        listener(entered, exited);
      } catch (e) {
        console.error('State change listener error:', e);
      }
    }
  }

  /**
   * Log a message (for <log> element)
   */
  _log(label, expr) {
    if (this._logger) {
      if (label) {
        this._logger(`[${label}] ${expr}`);
      } else {
        this._logger(expr);
      }
    }
  }

  /**
   * Raise an internal event (for <raise> element and done.state events)
   * @param eventName - The event name
   * @param data - Optional data payload (for done.state events with donedata)
   */
  _raise(eventName, data = null) {
    this.ctx.raiseEvent(eventName, data);
  }

  /**
   * Raise a platform event (for error events generated by the platform)
   */
  _raisePlatform(eventName, data = null) {
    this.ctx.raisePlatformEvent(eventName, data);
  }

  /**
   * Send an event (for <send> element)
   * @throws Error if target is invalid (per W3C spec, should abort executable content)
   */
  _send(eventName, options = {}) {
    const { target, type, delay, id, data } = options;

    // Helper to throw error with sendid for proper error event generation
    const throwError = (msg) => {
      const err = new Error(msg);
      err.sendid = id;
      throw err;
    };

    // Validate type - per W3C spec, invalid type should raise error.execution
    if (type !== undefined && type !== null && type !== '') {
      // Valid types: http://www.w3.org/TR/scxml/#SCXMLEventProcessor or 'scxml' shorthand
      const validTypes = [
        'http://www.w3.org/TR/scxml/#SCXMLEventProcessor',
        'scxml'
      ];
      if (!validTypes.includes(type)) {
        throwError(`error.execution: unsupported type '${type}'`);
      }
    }

    // Handle different targets
    if (target === '#_internal') {
      this.ctx.raiseEvent(eventName, data);
      return;
    }

    // Handle #_parent target - send event to parent machine (for invoked children)
    if (target === '#_parent') {
      if (this._parentMachine) {
        // Send event to parent machine with invokeid and origin info
        const eventOptions = {
          type: 'external',
          invokeid: this._invokeid,
          origin: '#_' + this._invokeid,
          origintype: 'http://www.w3.org/TR/scxml/#SCXMLEventProcessor'
        };

        const delayMs = this.ctx._parseDelay(delay);
        if (delayMs > 0) {
          // Delayed send to parent
          setTimeout(() => {
            if (!this._finished) {
              this._parentMachine.ctx.externalQueue.push(new ScxmlEvent(eventName, data, eventOptions));
              // Trigger parent to process the event
              if (this._parentMachine._processEventQueue) {
                this._parentMachine._processEventQueue();
              }
            }
          }, delayMs);
        } else {
          // Immediate send to parent
          this._parentMachine.ctx.externalQueue.push(new ScxmlEvent(eventName, data, eventOptions));
        }
        return;
      } else {
        // No parent machine - this is an error per W3C spec
        throwError(`error.communication: no parent machine to send to`);
      }
    }

    // Handle #_invokeid targets - send to invoked child machine
    if (target && target.startsWith('#_')) {
      const invokeid = target.substring(2); // Remove '#_' prefix

      // Check if this is an invokeid of a running child machine
      const invokeInfo = this.ctx.invokedMachines.get(invokeid);
      if (invokeInfo && invokeInfo.machine) {
        // Send event to child machine with origin and origintype (per W3C spec)
        const eventOptions = {
          type: 'external',
          origin: '#_scxml_' + this._sessionid,
          origintype: 'http://www.w3.org/TR/scxml/#SCXMLEventProcessor'
        };
        try {
          const delayMs = this.ctx._parseDelay(delay);
          if (delayMs > 0) {
            // Delayed send to child
            setTimeout(() => {
              if (invokeInfo.machine && !invokeInfo.machine.isFinished()) {
                invokeInfo.machine.send(eventName, data, eventOptions);
                if (invokeInfo.machine.isFinished()) {
                  this._handleInvokeCompletion(invokeid);
                }
              }
            }, delayMs);
          } else {
            invokeInfo.machine.send(eventName, data, eventOptions);
            if (invokeInfo.machine.isFinished()) {
              this._handleInvokeCompletion(invokeid);
            }
          }
        } catch (e) {
          // Ignore errors sending to child
        }
        return;
      }

      // Check other valid #_ targets
      if (!target.startsWith('#_scxml_') && target !== '#_internal') {
        // Unknown invokeid - not a valid target
        throwError(`error.execution: invalid target '${target}'`);
      }
    }

    // Validate target - per W3C spec
    if (target !== undefined && target !== null && target !== '') {
      // Check for #_scxml_* targets (session IDs)
      if (target.startsWith('#_scxml_')) {
        // Check if it's our own session
        if (target !== '#_scxml_' + this._sessionid) {
          // Per W3C spec: sending to non-existent session raises error.communication
          throwError(`error.communication: cannot dispatch to session '${target}'`);
        }
        // Sending to self - fall through to normal processing
      } else if (target !== '#_parent' && target !== '#_internal' &&
                 !target.startsWith('#_') && !target.startsWith('http://') && !target.startsWith('https://')) {
        // Invalid target format - raise error.execution
        throwError(`error.execution: invalid target '${target}'`);
      }
    }

    // Default: send to self via external queue
    // Include origin and origintype per W3C spec
    // Map 'id' to 'sendid' per W3C spec (_event.sendid should equal the send element's id)
    this.ctx.sendEvent(eventName, data, {
      delay,
      id,
      sendid: id,
      origin: '#_scxml_' + this._sessionid,
      origintype: 'http://www.w3.org/TR/scxml/#SCXMLEventProcessor'
    });
  }

  /**
   * Cancel a send (for <cancel> element)
   */
  _cancel(sendid) {
    this.ctx.cancelEvent(sendid);
  }

  /**
   * Check if a string is a valid JavaScript identifier
   */
  _isValidIdentifier(name) {
    return /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(name);
  }

  /**
   * Get valid datamodel keys (that can be used as function parameters)
   */
  _getValidDatamodelKeys() {
    return Object.keys(this._datamodel).filter(k => this._isValidIdentifier(k));
  }

  /**
   * Build a preamble string that re-declares all stored script functions.
   * By injecting function source code into the evaluation context (rather than
   * passing pre-created function objects as parameters), the functions are
   * re-created each time and their bodies bind to the CURRENT parameter values
   * (i.e., fresh datamodel values), not the stale values from when the script
   * was originally executed.
   */
  _getScriptFunctionPreamble() {
    const sources = this._scriptFunctionSources;
    if (!sources) return '';
    const parts = Object.values(sources);
    return parts.length > 0 ? parts.join('\n') + '\n' : '';
  }

  /**
   * Evaluate a condition expression.
   * Per W3C SCXML spec: If evaluation fails, the condition evaluates to false
   * AND error.execution is raised.
   */
  _evalCond(expr) {
    try {
      // Create function with data model variables in scope.
      // Script-defined functions are re-declared (not passed as closures) so they
      // bind to the CURRENT datamodel values rather than stale captured values.
      const validKeys = this._getValidDatamodelKeys();
      const allValues = validKeys.map(k => this._datamodel[k]);

      // Prepend stored function sources so they are re-created with fresh params
      const fnPreamble = this._getScriptFunctionPreamble();

      // Remove trailing semicolon(s) from expression
      const cleanExpr = expr.replace(/;+\s*$/, '');
      const fn = new Function(...validKeys, '_event', '_sessionid', '_name', '_ioprocessors', 'In',
        `${fnPreamble}return (${cleanExpr})`);
      return !!fn.call(this, ...allValues, this._event, this._sessionid, this._name, this._ioprocessors,
        (stateId) => this.isInState(stateId));
    } catch (e) {
      // Per W3C spec: invalid condition evaluates to false AND raises error.execution
      this._raisePlatform('error.execution');
      return false;
    }
  }

  /**
   * Evaluate an expression and return result.
   * If throwOnError is true, throws on evaluation failure (for foreach actions).
   */
  _evalExpr(expr, throwOnError = false) {
    try {
      // Create function with data model variables in scope.
      // Script-defined functions are re-declared (not passed as closures) so they
      // bind to the CURRENT datamodel values rather than stale captured values.
      const validKeys = this._getValidDatamodelKeys();
      const allValues = validKeys.map(k => this._datamodel[k]);

      // Prepend stored function sources so they are re-created with fresh params
      const fnPreamble = this._getScriptFunctionPreamble();

      // Remove trailing semicolon(s) from expression to avoid syntax errors in return statement
      const cleanExpr = expr.replace(/;+\s*$/, '');
      const fn = new Function(...validKeys, '_event', '_sessionid', '_name', '_ioprocessors', 'In',
        `${fnPreamble}return (${cleanExpr})`);
      return fn.call(this, ...allValues, this._event, this._sessionid, this._name, this._ioprocessors,
        (stateId) => this.isInState(stateId));
    } catch (e) {
      console.error('Expression evaluation error:', expr, e);
      if (throwOnError) {
        throw e;
      }
      return undefined;
    }
  }

  /**
   * Execute a script block and sync changes back to datamodel.
   * Also captures function declarations and stores them for use in expressions.
   */
  _execScript(script) {
    try {
      const validKeys = this._getValidDatamodelKeys();

      // Find function declarations in the script to capture them
      // Match: function funcName(
      const functionNameRegex = /function\s+([a-zA-Z_$][a-zA-Z0-9_$]*)\s*\(/g;
      const declaredFunctions = [];
      let match;
      while ((match = functionNameRegex.exec(script)) !== null) {
        declaredFunctions.push(match[1]);
      }

      // Build return statement to capture both datamodel vars and declared functions
      const returnParts = [
        ...validKeys.map(k => `${JSON.stringify(k)}: ${k}`),
        ...declaredFunctions.map(f => `${JSON.stringify(f)}: typeof ${f} === 'function' ? ${f} : undefined`)
      ];

      const wrappedScript = `
        ${script};
        return { ${returnParts.join(', ')} };
      `;

      // Prepend existing script function sources so they're available in this script
      const fnPreamble = this._getScriptFunctionPreamble();

      const fn = new Function(...validKeys, '_event', '_sessionid', '_name', '_ioprocessors', 'In',
        fnPreamble + wrappedScript);
      const result = fn.call(this, ...validKeys.map(k => this._datamodel[k]),
        this._event, this._sessionid, this._name, this._ioprocessors,
        (stateId) => this.isInState(stateId));

      // Sync changes back to datamodel
      if (result && typeof result === 'object') {
        for (const key of validKeys) {
          if (result.hasOwnProperty(key)) {
            this._datamodel[key] = result[key];
          }
        }
        // Store function SOURCE CODE (not objects) for use in _evalCond/_evalExpr.
        // Function.prototype.toString() returns the full declaration text.
        // Re-declaring from source in each evaluation context ensures the
        // function body references CURRENT datamodel parameter values, not the
        // stale values captured at the time of original script execution.
        for (const funcName of declaredFunctions) {
          if (result[funcName] && typeof result[funcName] === 'function') {
            this._scriptFunctionSources[funcName] = result[funcName].toString();
          }
        }
      }
    } catch (e) {
      console.error('Script execution error:', e);
      throw e;
    }
  }

  /**
   * Assign a value to a data model location
   */
  _assign(location, value) {
    // Simple assignment for top-level variables
    if (this._datamodel.hasOwnProperty(location)) {
      this._datamodel[location] = value;
    } else {
      // Try to evaluate as a path (e.g., "obj.prop")
      try {
        const fn = new Function(...Object.keys(this._datamodel), 'value',
          `${location} = value`);
        fn.call(this, ...Object.values(this._datamodel), value);
      } catch (e) {
        console.error('Assignment error:', location, e);
      }
    }
  }

  /**
   * Validate location and assign value - throws on invalid location
   * Per W3C SCXML spec, invalid locations should raise error.execution
   */
  _validateAndAssign(location, value) {
    // Check if location is a valid ECMAScript identifier or path
    // Valid patterns: single identifier (Var1), property path (obj.prop), array access (arr[0])
    const validPattern = /^[a-zA-Z_$][a-zA-Z0-9_$]*(\.[a-zA-Z_$][a-zA-Z0-9_$]*|\[[^\]]+\])*$/;

    if (!validPattern.test(location)) {
      throw new Error('error.execution: invalid location \'' + location + '\'');
    }

    // Check for read-only system variables (per W3C SCXML spec)
    const readOnlyVars = ['_sessionid', '_name', '_ioprocessors', '_event'];
    if (readOnlyVars.includes(location) || location.startsWith('_event.')) {
      throw new Error('error.execution: cannot assign to read-only variable \'' + location + '\'');
    }

    // Simple assignment for top-level variables
    if (this._datamodel.hasOwnProperty(location)) {
      this._datamodel[location] = value;
    } else {
      // Try to evaluate as a path (e.g., "obj.prop" or "arr[0]")
      // This modifies the object in-place through reference
      try {
        const validKeys = this._getValidDatamodelKeys();
        const fnPreamble = this._getScriptFunctionPreamble();
        const fn = new Function(...validKeys, 'value',
          `${fnPreamble}${location} = value`);
        fn.call(this, ...validKeys.map(k => this._datamodel[k]), value);
      } catch (e) {
        throw new Error('error.execution: ' + e.message);
      }
    }
  }

  // ============================================
  // Invoke support (per W3C SCXML spec)
  // ============================================

  /**
   * Start an invoked state machine.
   * Per W3C SCXML spec:
   * - Invokes are started when entering a state
   * - Each invoke has a unique invokeid
   * - When invoked machine reaches final state, done.invoke.<invokeid> is sent
   * - Autoforward causes events to be forwarded to child
   */
  _startInvoke(invokeid, options = {}) {
    const { type, src, content, contentExpr, childClass, autoforward, data, finalize, parentState } = options;

    // Validate type - must be SCXML type or something we support
    const scxmlTypes = [
      'http://www.w3.org/TR/scxml/',
      'http://www.w3.org/TR/scxml',
      'scxml'
    ];
    const isScxmlType = !type || scxmlTypes.some(t => type === t || type.startsWith(t));

    if (!isScxmlType) {
      // Unsupported invoke type - raise error.execution
      this._raisePlatform('error.execution');
      return;
    }

    let childMachine;

    // Option 1: Pre-compiled child class (full transpiled speed!)
    if (childClass) {
      try {
        childMachine = new childClass();
        // Set up parent-child communication
        childMachine._parentMachine = this;
        childMachine._invokeid = invokeid;
        // Initialize data from parent (per W3C spec: only set variables that are declared in child's datamodel)
        if (data && childMachine._datamodel) {
          for (const [key, value] of Object.entries(data)) {
            // Only set if the variable is already declared in child's datamodel
            if (childMachine._datamodel.hasOwnProperty(key)) {
              childMachine._datamodel[key] = value;
            }
          }
        }
      } catch (e) {
        this._raisePlatform('error.execution');
        return;
      }
    } else {
      // Option 2: Check registry or runtime parsing (fallback for dynamic content)

      // For src/srcexpr, first check if we have a bundled pre-compiled child in _invokeRegistry
      if (src) {
        const registry = this.constructor._invokeRegistry;
        if (registry) {
          // Try exact match
          let ChildClass = registry[src];
          // Try with/without file: prefix
          if (!ChildClass && src.startsWith('file:')) {
            ChildClass = registry[src.substring(5)];
          }
          if (!ChildClass && !src.startsWith('file:')) {
            ChildClass = registry['file:' + src];
          }

          if (ChildClass) {
            // Found bundled child - use pre-compiled class (full transpiled speed!)
            try {
              childMachine = new ChildClass();
              childMachine._parentMachine = this;
              childMachine._invokeid = invokeid;
              if (data && childMachine._datamodel) {
                for (const [key, value] of Object.entries(data)) {
                  if (childMachine._datamodel.hasOwnProperty(key)) {
                    childMachine._datamodel[key] = value;
                  }
                }
              }
            } catch (e) {
              this._raisePlatform('error.execution');
              return;
            }
          }
        }
      }

      // If not found in registry, try loader or inline content
      if (!childMachine) {
        let scxmlContent = content || contentExpr;

        // For src/srcexpr, we need an external loader
        if (src && !scxmlContent) {
          if (this._invokeLoader) {
            try {
              scxmlContent = this._invokeLoader(src);
            } catch (e) {
              this._raisePlatform('error.communication');
              return;
            }
          } else {
            this._raisePlatform('error.communication');
            return;
          }
        }

        if (!scxmlContent) {
          this._raisePlatform('error.execution');
          return;
        }

        // Create child machine from inline content using MinimalInvokeMachine
        childMachine = this._createChildMachine(scxmlContent, invokeid, data);
        if (!childMachine) {
          this._raisePlatform('error.execution');
          return;
        }
      }
    }

    // Store invoke info
    // Track if this is a pre-compiled child (which handles done.invoke via _setFinished)
    // Pre-compiled children can come from childClass option or from _invokeRegistry lookup
    const isPrecompiledChild = !!childClass || (childMachine && childMachine._parentMachine === this);
    this.ctx.invokedMachines.set(invokeid, {
      machine: childMachine,
      autoforward: !!autoforward,
      finalize: finalize,
      parentState: parentState,
      isPrecompiledChild: isPrecompiledChild
    });

    // Start the child machine
    try {
      childMachine.start();

      // Check if child immediately finished (reached final state)
      // For pre-compiled children, done.invoke is sent by _setFinished(), so skip _handleInvokeCompletion
      // For MinimalInvokeMachine, we need _handleInvokeCompletion to send done.invoke
      if (childMachine.isFinished() && !isPrecompiledChild) {
        this._handleInvokeCompletion(invokeid);
      }
    } catch (e) {
      // Invocation failed
      this._raisePlatform('error.execution');
      this.ctx.invokedMachines.delete(invokeid);
    }
  }

  /**
   * Create a child machine from SCXML content.
   * This is a minimal interpreter for inline SCXML content.
   */
  _createChildMachine(scxmlContent, invokeid, initialData) {
    try {
      // Parse the minimal SCXML content
      // For W3C tests, inline content is typically simple (e.g., just a final state)
      const parser = typeof DOMParser !== 'undefined' ? new DOMParser() : null;
      if (!parser) {
        // No DOM parser available
        return null;
      }

      const doc = parser.parseFromString(scxmlContent, 'text/xml');
      const scxmlEl = doc.documentElement;

      if (!scxmlEl || scxmlEl.nodeName !== 'scxml') {
        return null;
      }

      // Create a minimal child machine
      const childMachine = new MinimalInvokeMachine(scxmlEl, this, invokeid, initialData);
      return childMachine;
    } catch (e) {
      console.error('Failed to create child machine:', e);
      return null;
    }
  }

  /**
   * Handle completion of an invoked machine.
   * Sends done.invoke.<invokeid> event to parent.
   * Note: We do NOT delete the invoke info here - it must remain available for
   * finalize callbacks on events already in the queue. The invoke info is only
   * cleaned up when the parent exits the invoking state via _cancelInvoke.
   */
  _handleInvokeCompletion(invokeid, doneData = null) {
    const invokeInfo = this.ctx.invokedMachines.get(invokeid);
    if (!invokeInfo) return;

    // Mark the machine as completed (but keep invoke info for finalize)
    invokeInfo.completed = true;

    // Send done.invoke.<invokeid> event
    const eventName = 'done.invoke.' + invokeid;
    this.ctx.externalQueue.push(new ScxmlEvent(eventName, doneData, {
      type: 'external',
      invokeid: invokeid
    }));
  }

  /**
   * Cancel an invoked state machine.
   * Per W3C SCXML spec, invokes are cancelled when exiting the invoking state.
   */
  _cancelInvoke(invokeid) {
    const invokeInfo = this.ctx.invokedMachines.get(invokeid);
    if (!invokeInfo) return;

    // Stop the child machine (if it supports stopping)
    if (invokeInfo.machine && invokeInfo.machine.stop) {
      try {
        invokeInfo.machine.stop();
      } catch (e) {
        // Ignore errors during cancellation
      }
    }

    // Mark as cancelled so events from this invoke get filtered
    // Per W3C spec, events from cancelled invokes should be discarded
    invokeInfo.cancelled = true;
  }

  /**
   * Forward event to invoked machines if autoforward is enabled.
   * Called before processing each external event.
   */
  _forwardToInvokes(event) {
    for (const [invokeid, info] of this.ctx.invokedMachines) {
      if (info.autoforward && info.machine) {
        try {
          // Send the event to the child (with invokeid set)
          info.machine.send(event.name, event.data);

          // Check if child finished after processing this event
          if (info.machine.isFinished()) {
            this._handleInvokeCompletion(invokeid);
          }
        } catch (e) {
          // Ignore errors during forwarding
        }
      }
    }
  }

  /**
   * Set an invoke loader function for loading external SCXML sources.
   * The loader receives a src URL and should return SCXML content string.
   */
  setInvokeLoader(loader) {
    this._invokeLoader = loader;
  }

  /**
   * Filters conflicting transitions per W3C SCXML algorithm.
   * Transitions conflict if their exit sets have non-empty intersection.
   * Keeps higher priority (earlier in array).
   * In base class to avoid generating identical code in every machine.
   */
  _filterConflictingTransitions(transitions) {
    const result = [];
    for (const t of transitions) {
      let dominated = false;
      for (const existing of result) {
        if (existing.exitSet && t.exitSet) {
          for (const s of existing.exitSet) {
            if (t.exitSet.has(s)) {
              dominated = true;
              break;
            }
          }
          if (dominated) break;
        }
      }
      if (!dominated) result.push(t);
    }
    return result;
  }

  /**
   * W3C SCXML "optimal enablement" algorithm for event processing.
   * In base class to avoid generating ~110 lines of identical code per machine.
   * Generated classes override: _getStateChildren, _getStateParent, _getStateOrder,
   * _getEnabledTransitionForState, _checkEventlessTransitions.
   */
  _processEvent(event) {
    this._event = event;

    if (this._traceListener) this._traceListener.onEventQueue(event.name, event.type || 'external', null, Date.now());

    // Pre-allocated snapshot
    const activeSnapshot = this._dispatchSnapshot;
    activeSnapshot.clear();
    for (const s of this.activeStates) activeSnapshot.add(s);
    const enabledTransitions = [];
    const addedTransitions = this._dispatchAdded;
    addedTransitions.clear();

    // Find atomic states (active states with no active children)
    const atomicStates = [];
    for (const stateId of activeSnapshot) {
      let hasActiveChild = false;
      const children = this._getStateChildren(stateId);
      for (const childId of children) {
        if (activeSnapshot.has(childId)) { hasActiveChild = true; break; }
      }
      if (!hasActiveChild) atomicStates.push(stateId);
    }

    atomicStates.sort((a, b) => this._getStateOrder(a) - this._getStateOrder(b));

    // For each atomic state, traverse lineage and select first matching transition
    for (const atomicState of atomicStates) {
      let currentState = atomicState;
      while (currentState) {
        const t = this._getEnabledTransitionForState(currentState, event);
        if (t) {
          const key = t.source + ':' + t.priority;
          if (!addedTransitions.has(key)) {
            addedTransitions.add(key);
            enabledTransitions.push(t);
          }
          break;
        }
        currentState = this._getStateParent(currentState);
      }
    }

    enabledTransitions.sort((a, b) => a.priority - b.priority);
    const filtered = this._filterConflictingTransitions(enabledTransitions);

    for (const t of filtered) { t.execute(); }

    this._checkEventlessTransitions();

    if (this._traceListener) this._traceListener.onEventProcess(event.name, true, Date.now());

    this._event = null;
  }

  // Default no-ops for methods overridden by generated code
  _getStateChildren(stateId) { return []; }
  _getStateParent(stateId) { return null; }
  _getStateOrder(stateId) { return 9999; }
  _getEnabledTransitionForState(stateId, event) { return null; }
  _checkEventlessTransitions() {}
}

/**
 * Minimal state machine for invoked inline content.
 * Handles simple SCXML patterns used in W3C conformance tests.
 */
class MinimalInvokeMachine {
  constructor(scxmlElement, parent, invokeid, initialData) {
    this._parent = parent;
    this._invokeid = invokeid;
    this._finished = false;
    this._activeStates = new Set();
    this._datamodel = {}; // Start empty - will be populated during parse
    this._initialData = initialData || {}; // Store for later use
    this._declaredDataIds = new Set(); // Track declared data IDs
    this._states = new Map();
    this._initialState = null;
    this._pendingTimers = new Set(); // Track pending delayed sends (timer IDs)
    this._scheduledSendIds = new Set(); // Track scheduled event IDs for cancellation
    this._internalQueue = []; // Queue for events to self
    this._event = null; // Current event being processed (for _event access in expressions)

    // Parse the SCXML structure
    this._parseScxml(scxmlElement);
  }

  _parseScxml(scxmlEl) {
    // Get initial state
    this._initialState = scxmlEl.getAttribute('initial');

    // Parse states
    const stateEls = scxmlEl.children || scxmlEl.childNodes;
    let firstStateId = null;
    let stateIndex = 0;

    for (let i = 0; i < stateEls.length; i++) {
      const el = stateEls[i];
      if (el.nodeType !== 1) continue; // Skip non-elements

      const nodeName = el.nodeName || el.tagName;
      if (nodeName === 'state' || nodeName === 'final') {
        // Generate ID if not specified
        let stateId = el.getAttribute('id');
        if (!stateId) {
          stateId = '__state_' + stateIndex;
        }
        const isFinal = nodeName === 'final';

        if (!firstStateId) firstStateId = stateId;

        this._states.set(stateId, {
          id: stateId,
          isFinal: isFinal,
          transitions: this._parseTransitions(el),
          onentry: this._parseActions(el, 'onentry'),
          onexit: this._parseActions(el, 'onexit')
        });

        stateIndex++;
      } else if (nodeName === 'datamodel') {
        this._parseDatamodel(el);
      }
    }

    // Default to first state if no initial specified
    if (!this._initialState && firstStateId) {
      this._initialState = firstStateId;
    }
  }

  _parseDatamodel(datamodelEl) {
    const dataEls = datamodelEl.children || datamodelEl.childNodes;
    for (let i = 0; i < dataEls.length; i++) {
      const el = dataEls[i];
      if (el.nodeType !== 1) continue;
      if ((el.nodeName || el.tagName) === 'data') {
        const id = el.getAttribute('id');
        const expr = el.getAttribute('expr');
        if (id) {
          // Track that this variable is declared
          this._declaredDataIds.add(id);

          // Per W3C SCXML: data passed from parent (via namelist/param) overrides child's initial values
          // But only if the variable IS declared in the child's datamodel
          if (this._initialData.hasOwnProperty(id)) {
            // Use value from parent
            this._datamodel[id] = this._initialData[id];
          } else {
            // Use declared expression
            try {
              this._datamodel[id] = expr ? eval(expr) : undefined;
            } catch (e) {
              this._datamodel[id] = undefined;
            }
          }
        }
      }
    }
  }

  _parseTransitions(stateEl) {
    const transitions = [];
    const children = stateEl.children || stateEl.childNodes;
    for (let i = 0; i < children.length; i++) {
      const el = children[i];
      if (el.nodeType !== 1) continue;
      if ((el.nodeName || el.tagName) === 'transition') {
        // Parse actions inside the transition
        const actions = this._parseTransitionActions(el);
        transitions.push({
          event: el.getAttribute('event'),
          target: el.getAttribute('target'),
          cond: el.getAttribute('cond'),
          actions: actions
        });
      }
    }
    return transitions;
  }

  _parseTransitionActions(transEl) {
    const actions = [];
    const children = transEl.children || transEl.childNodes;
    for (let i = 0; i < children.length; i++) {
      const el = children[i];
      if (el.nodeType !== 1) continue;
      const nodeName = el.nodeName || el.tagName;
      if (nodeName === 'send') {
        actions.push({
          type: 'send',
          event: el.getAttribute('event'),
          target: el.getAttribute('target')
        });
      } else if (nodeName === 'raise') {
        actions.push({
          type: 'raise',
          event: el.getAttribute('event')
        });
      } else if (nodeName === 'assign') {
        actions.push({
          type: 'assign',
          location: el.getAttribute('location'),
          expr: el.getAttribute('expr')
        });
      }
    }
    return actions;
  }

  _parseActions(stateEl, actionType) {
    const actions = [];
    const children = stateEl.children || stateEl.childNodes;
    for (let i = 0; i < children.length; i++) {
      const el = children[i];
      if (el.nodeType !== 1) continue;
      if ((el.nodeName || el.tagName) === actionType) {
        // Parse actions inside onentry/onexit
        const actionChildren = el.children || el.childNodes;
        for (let j = 0; j < actionChildren.length; j++) {
          const actionEl = actionChildren[j];
          if (actionEl.nodeType !== 1) continue;
          const actionName = actionEl.nodeName || actionEl.tagName;
          if (actionName === 'send') {
            // Parse <param> children for event data
            const params = this._parseSendParams(actionEl);
            actions.push({
              type: 'send',
              event: actionEl.getAttribute('event'),
              target: actionEl.getAttribute('target'),
              delay: actionEl.getAttribute('delay'),
              delayexpr: actionEl.getAttribute('delayexpr'),
              params: params
            });
          } else if (actionName === 'raise') {
            actions.push({
              type: 'raise',
              event: actionEl.getAttribute('event')
            });
          }
        }
      }
    }
    return actions;
  }

  _parseSendParams(sendEl) {
    const params = {};
    const children = sendEl.children || sendEl.childNodes;
    for (let i = 0; i < children.length; i++) {
      const el = children[i];
      if (el.nodeType !== 1) continue;
      if ((el.nodeName || el.tagName) === 'param') {
        const name = el.getAttribute('name');
        const expr = el.getAttribute('expr');
        if (name && expr) {
          try {
            // Evaluate the expression
            params[name] = eval(expr);
          } catch (e) {
            params[name] = undefined;
          }
        }
      }
    }
    return params;
  }

  start() {
    if (this._initialState) {
      this._enterState(this._initialState);
      // Check eventless transitions after entering initial state
      this._checkEventlessTransitions();
    }
  }

  _checkEventlessTransitions() {
    let changed = true;
    let iterations = 0;
    const maxIterations = 100;

    while (changed && iterations < maxIterations && !this._finished) {
      changed = false;
      iterations++;

      // Check each active state for eventless transitions
      for (const stateId of Array.from(this._activeStates)) {
        if (this._finished) break;
        const state = this._states.get(stateId);
        if (!state) continue;

        for (const trans of state.transitions) {
          // Eventless transitions have no event attribute (null/undefined)
          if (!trans.event) {
            // Check condition if present
            let condMet = true;
            if (trans.cond) {
              try {
                const validKeys = Object.keys(this._datamodel);
                const fn = new Function(...validKeys, `return (${trans.cond})`);
                const validValues = validKeys.map(k => this._datamodel[k]);
                condMet = !!fn.call(this, ...validValues);
              } catch (e) {
                condMet = false;
              }
            }

            if (condMet) {
              // Exit current state
              this._exitState(stateId);

              // Execute transition actions
              if (trans.actions && trans.actions.length > 0) {
                for (const action of trans.actions) {
                  this._executeAction(action);
                }
              }

              // Enter target state
              if (trans.target) {
                this._enterState(trans.target);
              }

              changed = true;
              break; // Restart checking from the beginning
            }
          }
        }
        if (changed) break;
      }
    }
  }

  stop() {
    this._finished = true;
    // Cancel all pending delayed sends (async timers)
    for (const timerId of this._pendingTimers) {
      clearTimeout(timerId);
    }
    this._pendingTimers.clear();

    // Cancel all scheduled events in parent's context
    for (const sendId of this._scheduledSendIds) {
      this._parent.ctx.scheduledEvents.delete(sendId);
    }
    this._scheduledSendIds.clear();
  }

  isFinished() {
    return this._finished;
  }

  send(eventName, data = null, options = {}) {
    if (this._finished) return;

    // Set up _event for access in expressions and actions (per W3C spec)
    this._event = {
      name: eventName,
      data: data,
      type: options.type || 'external',
      origin: options.origin || null,
      origintype: options.origintype || null,
      invokeid: options.invokeid || null,
      sendid: options.sendid || null
    };

    // Find matching transition in active states
    for (const stateId of this._activeStates) {
      const state = this._states.get(stateId);
      if (!state) continue;

      for (const trans of state.transitions) {
        if (this._matchEvent(eventName, trans.event)) {
          // Exit current state
          this._exitState(stateId);

          // Execute transition actions
          if (trans.actions && trans.actions.length > 0) {
            for (const action of trans.actions) {
              this._executeAction(action);
            }
          }

          // Enter target state
          if (trans.target) {
            this._enterState(trans.target);
          }

          // Check eventless transitions after state change
          this._checkEventlessTransitions();

          this._event = null;
          return;
        }
      }
    }

    this._event = null;
  }

  _matchEvent(eventName, pattern) {
    if (!pattern) return false;
    if (pattern === '*') return true;
    if (pattern === eventName) return true;
    if (eventName && eventName.startsWith(pattern + '.')) return true;
    return false;
  }

  _enterState(stateId) {
    const state = this._states.get(stateId);
    if (!state) return;

    this._activeStates.add(stateId);

    // Execute onentry actions
    for (const action of state.onentry) {
      this._executeAction(action);
    }

    // Check if this is a final state
    if (state.isFinal) {
      // Execute onexit actions before finishing (per W3C spec)
      for (const action of state.onexit) {
        this._executeAction(action);
      }
      this._finished = true;
    }
  }

  _exitState(stateId) {
    const state = this._states.get(stateId);
    if (!state) return;

    // Execute onexit actions
    for (const action of state.onexit) {
      this._executeAction(action);
    }

    this._activeStates.delete(stateId);
  }

  _executeAction(action) {
    if (action.type === 'send' && action.target === '#_parent') {
      // Calculate delay if present
      let delayMs = 0;
      if (action.delayexpr) {
        try {
          const delayStr = eval(action.delayexpr);
          delayMs = this._parseDelay(delayStr);
        } catch (e) {
          // Ignore delay errors
        }
      } else if (action.delay) {
        delayMs = this._parseDelay(action.delay);
      }

      // Send event to parent with params as data
      const eventData = action.params && Object.keys(action.params).length > 0 ? action.params : null;

      if (delayMs > 0) {
        // Delayed send - use timer and track for cancellation
        const timerId = setTimeout(() => {
          if (!this._finished) {
            this._pendingTimers.delete(timerId);
            this._parent.ctx.externalQueue.push(new ScxmlEvent(action.event, eventData, {
              type: 'external',
              invokeid: this._invokeid,
              origin: '#_' + this._invokeid,
              origintype: 'http://www.w3.org/TR/scxml/#SCXMLEventProcessor'
            }));
            // Trigger parent to process the event
            if (this._parent._processEventQueue) {
              this._parent._processEventQueue();
            }
          }
        }, delayMs);
        this._pendingTimers.add(timerId);
      } else {
        // Immediate send
        this._parent.ctx.externalQueue.push(new ScxmlEvent(action.event, eventData, {
          type: 'external',
          invokeid: this._invokeid,
          origin: '#_' + this._invokeid,
          origintype: 'http://www.w3.org/TR/scxml/#SCXMLEventProcessor'
        }));
      }
    } else if (action.type === 'send') {
      // Send to self - internal event for this child machine
      let delayMs = 0;
      if (action.delayexpr) {
        try {
          const delayStr = eval(action.delayexpr);
          delayMs = this._parseDelay(delayStr);
        } catch (e) {
          // Ignore delay errors
        }
      } else if (action.delay) {
        delayMs = this._parseDelay(action.delay);
      }

      if (delayMs > 0) {
        // Delayed internal event - use parent's scheduling system for synchronous test execution
        // Store in parent's scheduledEvents with a wrapper that routes to this child
        const sendId = action.id || ('__child_' + this._invokeid + '_' + Date.now() + '_' + Math.random());
        const childMachine = this;
        const eventName = action.event;

        // Schedule a callback event that will fire on this child
        const scheduledTime = Date.now() + delayMs;
        this._parent.ctx.scheduledEvents.set(sendId, {
          time: scheduledTime,
          event: new ScxmlEvent('__child_internal__' + sendId, { childMachine, eventName }, { type: 'external' }),
          childMachine: this,
          childEvent: eventName
        });
        this._scheduledSendIds.add(sendId);
      } else {
        // Queue as external event (will be processed after current processing)
        this._internalQueue.push(action.event);
      }
    } else if (action.type === 'raise') {
      // Internal event (not implemented for minimal machine)
    } else if (action.type === 'assign') {
      // Assign action - evaluate expression and store in datamodel
      try {
        // Build evaluation context with datamodel variables and _event
        const evalContext = { ...this._datamodel };
        if (this._event) {
          evalContext._event = this._event;
        }
        const keys = Object.keys(evalContext);
        const values = keys.map(k => evalContext[k]);
        const fn = new Function(...keys, `return (${action.expr})`);
        const value = fn.call(null, ...values);
        this._datamodel[action.location] = value;
      } catch (e) {
        // Ignore assign errors in minimal machine
      }
    }
  }

  _parseDelay(delayStr) {
    if (!delayStr) return 0;
    const str = String(delayStr).trim();
    if (str.endsWith('ms')) {
      return parseFloat(str);
    } else if (str.endsWith('s')) {
      return parseFloat(str) * 1000;
    }
    return parseFloat(str) || 0;
  }

  getActiveStateIds() {
    return new Set(this._activeStates);
  }
}






